# We've created lists by using square brackets [] around the data we want the list to contain.
# We can also create lists out of variables, rather than writing in the data directly.

grocery_a = 'chicken'
grocery_b = 'onions'
grocery_c = 'rice'
grocery_d = 'peppers'
grocery_e = 'bananas'

# generate with the data directly
grocery_list = ['chicken', 'onions', 'rice', 'peppers', 'bananas']
print(grocery_list)

# or with the variable
grocery_list = [grocery_a, grocery_b, grocery_c, grocery_d, grocery_e]
print(grocery_list)