# Choosing Types

# By now, you have learned about most of the common data structures in Python. 
# One of the challenges you might face is knowing when to use the various data types.

# When choosing a collection type, it is useful to understand the unique properties of that type. 
# For example, a list is for us to store multiple objects and to retain a sequence, a dictionary is for us to store unique key-value pair mappings, tuples are immutable, and sets only store unique elements. 
# Choosing the right type for a particular dataset could mean an increase in efficiency or security.

# Choosing an incorrect type for your data will lead to data loss, in most cases it leads to low efficiency while running our code, and in the worst case, we might lose our data.